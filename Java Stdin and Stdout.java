import java.util.*;

public class StdinStdout {
	public static void main (String[] args) {
		Scanner Sc = new Scanner(System.in);
		int i = Sc.nextInt();
		int j = Sc.nextInt();
		int k = Sc.nextInt();
		Sc.close();
		
		System.out.println(i);
		System.out.println(j);
		System.out.println(k);
				
	}

}
